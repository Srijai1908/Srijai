package operators;

public class Pattern2 {

	public static void main(String[] args) {
		int n=7;
		int i,j;
		for(i=1;i<=n;i++) {
			
			for(j=1;j<=i;j++) {
	        if(i==1) {
	        	System.out.print("A ");
	        }
	        else if(i==2) {
		System.out.print("B ");
}
	        else if(i==3) {
	        	System.out.print("C ");
	        }
	        else if(i==4) {
	        	System.out.print("D ");
	        }
	        else if(i==5) {
	        	System.out.print("E ");
	        }
	        else if(i==6) {
	        	System.out.print("F ");
	        }
			}
	        System.out.println();}

}
}
