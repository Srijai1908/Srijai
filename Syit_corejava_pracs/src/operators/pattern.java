package operators;

public class pattern {

	public static void main(String[] args) {
	        // No of rows to be printed
	        int n = 5;
	 
	        int i, j, k = 1;
	        for (i = 1; i <= n; i++) {
	 
	            for (j = 1; j <= i; j++) {
	
	       
	                System.out.print(k + "  ");
	 
	               
	                k++;
	            } 
	            System.out.println();
	        }
	    }
	
	}


